/**
 * @swagger
 * /api/auth/profile:
 *   put:
 *     summary: Update user profile
 *     tags:
 *       - Authentication
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               firstName:
 *                 type: string
 *                 example: John
 *               lastName:
 *                 type: string
 *                 example: Doe
 *               preferences:
 *                 type: object
 *                 properties:
 *                   dietaryRestrictions:
 *                     type: array
 *                     items:
 *                       type: string
 *                       enum: [vegetarian, vegan, gluten-free, dairy-free, nut-free, halal, kosher]
 *                   allergies:
 *                     type: array
 *                     items:
 *                       type: string
 *                   cuisinePreferences:
 *                     type: array
 *                     items:
 *                       type: string
 *               notifications:
 *                 type: object
 *                 properties:
 *                   emailNotifications:
 *                     type: boolean
 *                   expiryAlerts:
 *                     type: boolean
 *                   lowStockAlerts:
 *                     type: boolean
 *     responses:
 *       200:
 *         description: Profile updated successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                 user:
 *                   type: object
 *       400:
 *         description: Validation error
 *       401:
 *         description: Unauthorized
 */
router.put('/profile', protect, async (req, res) => {
  try {
    const allowedFields = ['firstName', 'lastName', 'preferences', 'notifications', 'avatar'];
    const updateData = {};

    allowedFields.forEach(field => {
      if (req.body[field] !== undefined) {
        updateData[field] = req.body[field];
      }
    });

    const user = await User.findByIdAndUpdate(
      req.user._id,
      updateData,
      { new: true, runValidators: true }
    );

    res.status(200).json({
      success: true,
      message: 'Profile updated successfully',
      user
    });
  } catch (error) {
    res.status(400).json({
      success: false,
      message: error.message
    });
  }
});

module.exports = router;